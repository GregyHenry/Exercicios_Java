
public class OperacoesMath {

	public static void calculoSoma(float numero1, float numero2) {

		float calculo = numero1 + numero2;
		System.out.println("Sua soma é de: " + calculo);

	}

	public static void calculoSubtracao(float numero1, float numero2) {

		float calculo = numero1 - numero2;
		System.out.println("Sua subtração é de: " + calculo);

	}

	public static void calculoDivisao(float numero1, float numero2) {

		float calculo = numero1 / numero2;
		System.out.println("Sua divisão é de: " + calculo);

	}

	public static void calculoMultiplicacao(float numero1, float numero2) {

		float calculo = numero1 * numero2;
		System.out.println("Sua multiplicação é de: " + calculo);

	}

}
